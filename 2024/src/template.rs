#![allow(unused_variables)]

pub fn part1(input: &str) -> String {
    "todo".to_string()
}

pub fn part2(input: &str) -> String {
    "todo".to_string()
}

#[cfg(test)]
mod tests {
    use super::*;

    const EXAMPLE1: &str = "";

    #[test]
    fn test_part1() {
        assert_eq!(part1(&EXAMPLE1), "todo");
    }

    #[test]
    fn test_part2() {
        assert_eq!(part2(&EXAMPLE1), "todo");
    }
}
